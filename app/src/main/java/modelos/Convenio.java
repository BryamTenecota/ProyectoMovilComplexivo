package modelos;

public class Convenio {
    private String descripcion;
    private String fecha_elaboracion;
    private String numero_convenio;
    private String numero_itv;

    public String getDescripcion() {
        return descripcion;
    }

    public void setDescripcion(String descripcion) {
        this.descripcion = descripcion;
    }

    public String getFecha_elaboracion() {
        return fecha_elaboracion;
    }

    public void setFecha_elaboracion(String fecha_elaboracion) {
        this.fecha_elaboracion = fecha_elaboracion;
    }

    public String getNumero_convenio() {
        return numero_convenio;
    }

    public void setNumero_convenio(String numero_convenio) {
        this.numero_convenio = numero_convenio;
    }

    public String getNumero_itv() {
        return numero_itv;
    }

    public void setNumero_itv(String numero_itv) {
        this.numero_itv = numero_itv;
    }
}
